-- Prediction output table for scoring jobs.
-- Keeps latest model outputs per order for queueing and auditability.

create table if not exists public.order_predictions (
  prediction_id bigint generated by default as identity primary key,
  order_id bigint not null unique references public.orders(order_id) on delete cascade,
  fraud_probability numeric(6,5),
  predicted_is_fraud boolean,
  late_delivery_probability numeric(6,5),
  predicted_late_delivery boolean,
  model_version text,
  prediction_timestamp timestamptz not null default now()
);

create index if not exists idx_order_predictions_order_id on public.order_predictions(order_id);
create index if not exists idx_order_predictions_late_delivery_probability
  on public.order_predictions(late_delivery_probability desc);
